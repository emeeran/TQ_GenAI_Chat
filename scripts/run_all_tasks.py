#!/usr/bin/env python3
"""
Run All Tasks Script

This script executes all the tasks from the TODO.md in sequence:
1. Update models from providers
2. Set default models
3. Declutter project (if needed)
4. Test app launch
5. Generate summary report
"""

import subprocess
import sys
import time
from pathlib import Path

# Project root directory
PROJECT_ROOT = Path(__file__).parent.parent.absolute()


def run_command(command, description, cwd=None):
    """Run a command and return success status"""
    print(f"\n🔄 {description}...")
    print(f"Command: {command}")

    try:
        result = subprocess.run(
            command, shell=True, cwd=cwd or PROJECT_ROOT, capture_output=True, text=True, timeout=60
        )

        if result.returncode == 0:
            print(f"✅ {description} completed successfully")
            if result.stdout.strip():
                print(f"Output: {result.stdout.strip()}")
            return True
        else:
            print(f"❌ {description} failed")
            if result.stderr.strip():
                print(f"Error: {result.stderr.strip()}")
            return False

    except subprocess.TimeoutExpired:
        print(f"⏰ {description} timed out")
        return False
    except Exception as e:
        print(f"❌ {description} failed with exception: {e}")
        return False


def task_1_update_models():
    """Task 1: Update models from providers"""
    return run_command(
        "python3 scripts/update_models_from_providers.py", "Updating models from providers"
    )


def task_2_test_defaults_persistence():
    """Task 2: Test defaults persistence"""
    test_script = """
import sys
from pathlib import Path
sys.path.insert(0, str(Path.cwd()))
from core.models import model_manager

# Test setting and getting default
provider = 'gemini'
model = 'gemini-2.0-flash'

print(f'Setting default for {provider}: {model}')
model_manager.set_default_model(provider, model)

retrieved = model_manager.get_default_model(provider)
print(f'Retrieved default for {provider}: {retrieved}')

if retrieved == model:
    print('✅ Defaults persistence working')
else:
    print('❌ Defaults persistence failed')
    sys.exit(1)
"""

    return run_command(f'python3 -c "{test_script}"', "Testing defaults persistence")


def task_3_declutter_if_needed():
    """Task 3: Declutter project if needed"""
    # Check if decluttering is needed
    trash_dir = PROJECT_ROOT / "trash2review"
    if not trash_dir.exists():
        return run_command("python3 scripts/organize_and_declutter.py", "Decluttering project")
    else:
        print("✅ Project already decluttered (trash2review exists)")
        return True


def task_4_test_app_launch():
    """Task 4: Test app launch"""
    return run_command(
        "timeout 5s python3 app.py || echo 'App launch test completed'", "Testing app launch"
    )


