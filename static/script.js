// Add request queue
const requestQueue = [];
let isProcessing = false;

async function processQueue() {
    if (isProcessing || requestQueue.length === 0) return;

    isProcessing = true;
    while (requestQueue.length > 0) {
        const request = requestQueue.shift();
        try {
            await request();
        } catch (error) {
            console.error('Request error:', error);
        }
    }
    isProcessing = false;
}

async function updateModels() {
    const provider = document.getElementById('provider').value;
    const modelSelect = document.getElementById('model');
    modelSelect.innerHTML = '<option value="">Loading models...</option>';
    modelSelect.disabled = true;

    try {
        const response = await fetch(`/get_models/${provider}`);
        if (!response.ok) {
            throw new Error(`HTTP error! status: ${response.status}`);
        }

        const data = await response.json();
        const models = data.models;
        const defaultModel = data.default;

        modelSelect.innerHTML = '<option value="">Select Model</option>';

        if (Array.isArray(models) && models.length > 0) {
            models.sort().forEach(model => {
                const option = document.createElement('option');
                option.value = model;
                option.textContent = model;
                if (model === defaultModel) {
                    option.selected = true;
                }
                modelSelect.appendChild(option);
            });
            modelSelect.disabled = false;
        } else {
            modelSelect.innerHTML = '<option value="">No models available</option>';
            modelSelect.disabled = true;
        }
    } catch (error) {
        console.error('Error fetching models:', error);
        modelSelect.innerHTML = '<option value="">Error loading models</option>';
        modelSelect.disabled = true;
    }
}

function appendMessage(message, isUser = false) {
    const chatBox = document.getElementById('chat-box');
    const messageDiv = document.createElement('div');
    messageDiv.className = `message ${isUser ? 'user-message' : 'ai-message'}`;

    if (isUser) {
        messageDiv.textContent = message;
    } else {
        // Handle formatted response
        const content = typeof message === 'object' ? message : { text: message };

        // Create main content
        if (content.html) {
            messageDiv.innerHTML = content.html;
        } else {
            messageDiv.textContent = content.text || message;
        }

        // Add metadata if available
        if (content.metadata) {
            const metadataDiv = document.createElement('div');
            metadataDiv.className = 'message-metadata';
            metadataDiv.innerHTML = `
                <small class="text-muted">
                    Generated by ${content.metadata.provider}/${content.metadata.model}
                    in ${content.metadata.response_time}
                </small>
            `;
            messageDiv.appendChild(metadataDiv);
        }

        // Add speak button
        const speakButton = document.createElement('button');
        speakButton.className = 'btn btn-sm btn-outline-secondary speak-message';
        speakButton.innerHTML = '<i class="fas fa-volume-up"></i>';
        speakButton.onclick = () => speakText(content.text || message);
        messageDiv.appendChild(speakButton);
    }

    chatBox.appendChild(messageDiv);
    chatBox.scrollTop = chatBox.scrollHeight;

    // Apply syntax highlighting to code blocks
    if (!isUser) {
        messageDiv.querySelectorAll('pre code').forEach((block) => {
            hljs.highlightElement(block);
        });
    }

    if (!isUser && message.text) {
        lastAiResponse = message.text;
    }
}

